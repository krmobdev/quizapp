from pathlib import Path

OUT = Path(__file__).parent


def write_bank(name: str, questions: list) -> None:
    path = OUT / f"bank_{name}.py"
    lines = ["# Auto-generated question bank\n", "QUESTIONS = [\n"]
    for text, opts, ci, expl, diff in questions:
        lines.append(f"    ({text!r}, {opts!r}, {ci}, {expl!r}, {diff!r}),\n")
    lines.append("]\n")
    path.write_text("".join(lines), encoding="utf-8")
    print(f"bank_{name}.py: {len(questions)} questions")


