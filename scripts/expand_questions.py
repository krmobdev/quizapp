def load_existing(category: str) -> list[dict]:
    path = ASSETS / f"questions_{category}.json"
    with path.open(encoding="utf-8") as f:
        return json.load(f)


def make_question(
    category: str,
    text: str,
    options: list[str],
    correct_index: int,
    explanation: str,
    difficulty: str,
) -> dict:
    return {
        "category": category,
        "difficulty": difficulty,
        "text": text,
        "options": options,
        "correctIndex": correct_index,
        "explanation": explanation,
    }


def shuffle_options(options: list[str], correct_index: int) -> tuple[list[str], int]:
    indexed = list(enumerate(options))
    random.shuffle(indexed)
    new_options = [opt for _, opt in indexed]
    new_correct = next(i for i, (old, _) in enumerate(indexed) if old == correct_index)
    return new_options, new_correct


def add_unique(pool: list[dict], seen: set[str], candidate: dict) -> bool:
    key = candidate["text"].strip().lower()
    if key in seen:
        return False
    seen.add(key)
    pool.append(candidate)
    return True


