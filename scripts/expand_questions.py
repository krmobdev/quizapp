def generate_math(seen: set[str], pool: list[dict], need: int) -> None:
    cat = "math"

    for text, options, correct, expl, diff in MATH_FACTS:
        q = make_question(cat, text, options, correct, expl, diff)
        if add_unique(pool, seen, q) and len(pool) >= need:
            return

    # Addition
    for a in range(2, 120):
        for b in range(2, 80):
            ans = a + b
            wrong = {ans - 1, ans + 1, ans + 2, ans - 2, a + b + 3}
            wrong.discard(ans)
            opts = [str(ans)] + [str(x) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {a} + {b}?",
                opts,
                opts.index(str(ans)),
                f"{a} + {b} = {ans}.",
                "EASY" if a + b < 50 else "MEDIUM",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return

    # Multiplication
    for a in range(2, 25):
        for b in range(2, 25):
            ans = a * b
            wrong = {ans - a, ans + b, ans + 1, a * (b + 1)}
            wrong.discard(ans)
            opts = [str(ans)] + [str(x) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {a} × {b}?",
                opts,
                opts.index(str(ans)),
                f"{a} × {b} = {ans}.",
                "EASY" if a * b <= 50 else "MEDIUM",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return

    # Subtraction
    for a in range(20, 200):
        for b in range(5, min(a, 50)):
            ans = a - b
            wrong = {ans - 1, ans + 1, ans + 2, b}
            wrong.discard(ans)
            opts = [str(ans)] + [str(x) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {a} − {b}?",
                opts,
                opts.index(str(ans)),
                f"{a} − {b} = {ans}.",
                "MEDIUM",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return

    # Division (exact)
    for b in range(2, 13):
        for ans in range(2, 13):
            a = b * ans
            wrong = {ans - 1, ans + 1, ans + 2, b}
            wrong.discard(ans)
            opts = [str(ans)] + [str(x) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {a} ÷ {b}?",
                opts,
                opts.index(str(ans)),
                f"{a} ÷ {b} = {ans}.",
                "MEDIUM",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return

    # Percentages
    for pct in range(5, 100, 5):
        for base in range(20, 500, 20):
            ans = pct * base // 100
            if ans == 0:
                continue
            wrong = {ans - 1, ans + 1, ans + pct, ans - pct}
            wrong.discard(ans)
            opts = [str(ans)] + [str(max(0, x)) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {pct}% от {base}?",
                opts,
                opts.index(str(ans)),
                f"{pct}% от {base} = {ans}.",
                "HARD",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return

    # Powers
    for base in range(2, 12):
        for exp in range(2, 6):
            ans = base ** exp
            wrong = {ans - base, ans + 1, (base + 1) ** exp, base ** (exp - 1) if exp > 2 else ans + 2}
            wrong.discard(ans)
            opts = [str(ans)] + [str(x) for x in list(wrong)[:3]]
            random.shuffle(opts)
            q = make_question(
                cat,
                f"Сколько будет {base} в степени {exp}?",
                opts,
                opts.index(str(ans)),
                f"{base}^{exp} = {ans}.",
                "HARD",
            )
            if add_unique(pool, seen, q) and len(pool) >= need:
                return


